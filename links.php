			<h2><u>PAN Card - Permanent Account Number (PAN)</u></h2>
			<p>
				Permanent Account Number or PAN is a means of identifying various taxpayers in the country. PAN is a 10-digit unique identification alphanumeric number (containing both alphabets and numbers) assigned to Indians, mostly to those who pay tax.<br><br>

				The PAN system of identification is a computer-based system that assigns unique identification number to every Indian tax paying entity. Through this method, all tax-related information for a person is recorded against a single PAN number which acts as the primary key for storage of information. This is shared across the country and hence no two people on tax paying entities can have the same PAN.<br><br>

				When PAN is allotted to an entity, PAN Card too is given by the Income Tax Department. While PAN is a number, PAN Card is a physical card that has your PAN as well as name, date of birth (DoB), and photograph. Copies of this card can be submitted as proof of identity or DoB.<br><br>

				Your PAN Card is valid for lifetime because it is unaffected by any change in address.</p>
